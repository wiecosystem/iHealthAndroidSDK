package com.ihealth.communication.task;

public class iHDtask {
	
	private AbstractTask mAbstractTask;
	private String mTaskMethod;
	private Object[] mTaskPara;
	
	public iHDtask(AbstractTask mAbstractTask, String mTaskMethod,
			Object[] mTaskPara) {
		this.mAbstractTask = mAbstractTask;
		this.mTaskMethod = mTaskMethod;
		this.mTaskPara = mTaskPara;
	}

	public iHDtask() {}

	public AbstractTask getmAbstractTask() {
		return mAbstractTask;
	}

	public String getmTaskMethod() {
		return mTaskMethod;
	}

	public Object[] getmTaskPara() {
		return mTaskPara;
	}

}
