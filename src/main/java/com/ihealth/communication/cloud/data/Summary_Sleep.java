package com.ihealth.communication.cloud.data;

public class Summary_Sleep {

	private int id;//标识
	private String overTime;//结束时间（年－月－日 时－分）
	private int useTime;//sleep分钟数
	private float sleepEfficiency;//sleep效率
	private int isDelayFifty;//是否延迟睡眠（50分钟）
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getOverTime() {
		return overTime;
	}
	public void setOverTime(String overTime) {
		this.overTime = overTime;
	}
	public int getUseTime() {
		return useTime;
	}
	public void setUseTime(int useTime) {
		this.useTime = useTime;
	}
	public float getSleepEfficiency() {
		return sleepEfficiency;
	}
	public void setSleepEfficiency(float sleepEfficiency) {
		this.sleepEfficiency = sleepEfficiency;
	}
	public int getIsDelayFifty() {
		return isDelayFifty;
	}
	public void setIsDelayFifty(int isDelayFifty) {
		this.isDelayFifty = isDelayFifty;
	}
	
}
