package com.ihealth.communication.cloud;

import java.util.ArrayList;


public class ReturnDataCenter {

	private String content;
	private ArrayList<ApiData> apiInfo = new ArrayList<ApiData>();
	private String AuthorizationTitle;
	private String returncode;
	
	public String getReturncode() {
		return returncode;
	}
	public void setReturncode(String returncode) {
		this.returncode = returncode;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public ArrayList<ApiData> getApiInfo() {
		return apiInfo;
	}
	public void setApiInfo(ArrayList<ApiData> apiInfo) {
		this.apiInfo = apiInfo;
	}
	public String getAuthorizationTitle() {
		return AuthorizationTitle;
	}
	public void setAuthorizationTitle(String authorizationTitle) {
		AuthorizationTitle = authorizationTitle;
	}
	
}
