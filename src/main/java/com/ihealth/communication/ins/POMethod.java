    private static boolean isWrongTime(Context context, byte[] bs, String mac, String firmware) {
        try {
            long currentTime = System.currentTimeMillis() / 1000;
            String str = getOfflinedate(bs);
            str = changeOfflineData(context, mac, firmware, str);
            int year = Integer.valueOf((String) str.subSequence(0, 4));
            int month = Integer.valueOf((String) str.subSequence(5, 7));
            int day = Integer.valueOf((String) str.subSequence(8, 10));
            int hour = Integer.valueOf((String) str.subSequence(11, 13));
            int minite = Integer.valueOf((String) str.subSequence(14, 16));
            int second = Integer.valueOf((String) str.subSequence(17, 19));
            long measureTime = ByteBufferUtil.BirthdayToLong(str);
            if (measureTime > currentTime) {
                return true;
            }

            if (!String.valueOf(year).startsWith("20")) {
                return true;
            }
            if (month <= 0 || month > 12) {
                return true;
            }
            if (month == 1 || month == 3 || month == 5 || month == 7 || month == 8 || month == 10 || month == 12) {
                if (day <= 0 || day > 31) {
                    return true;
                }
            }
            if (month == 4 || month == 6 || month == 9 || month == 11) {
                if (day <= 0 || day > 30) {
                    return true;
                }
            }
            if (month == 2) {
                if ((year % 4 == 0 && year % 100 != 0) || (year % 400 == 0)) { // 是闰年
                    if (day <= 0 || day > 29) {
                        return true;
                    }
                } else {
                    if (day <= 0 || day > 28) { // 不是闰年
                        return true;
                    }
                }

            }
            if (hour < 0 || hour > 24) {
                return true;
            }
            if (minite < 0 || minite > 60) {
                return true;
            }
            if (second < 0 || second > 60) {
                return true;
            }
            return false;
        } catch (Exception e) {
            Log.p(TAG, Log.Level.WARN, "Exception", e.getMessage());
            return false;
        }

    }

    // time form：2015-08-21 8：46：21
    private static String getOfflinedate(byte[] bs) {
        String year = String.valueOf(((int) bs[0] & 0xFF) + 2000);
        String month;
        if ((bs[1] & 0xFF) < 10) {
            month = "0" + String.valueOf(bs[1] & 0xFF);
        } else {
            month = String.valueOf(bs[1] & 0xFF);
        }

        String day;
        if ((bs[2] & 0xFF) < 10) {
            day = "0" + String.valueOf(bs[2] & 0xFF);
        } else {
            day = String.valueOf(bs[2] & 0xFF);
        }

        String hour;
        if ((bs[3] & 0xFF) < 10) {
            hour = "0" + String.valueOf(bs[3] & 0xFF);
        } else {
            hour = String.valueOf(bs[3] & 0xFF);
        }

        String mins;
        if ((bs[4] & 0xFF) < 10) {
            mins = "0" + String.valueOf(bs[4] & 0xFF);
        } else {
            mins = String.valueOf(bs[4] & 0xFF);
        }

        String sed;
        if ((bs[5] & 0xFF) < 10) {
            sed = "0" + String.valueOf(bs[5] & 0xFF);
        } else {
            sed = String.valueOf(bs[5] & 0xFF);
        }
        return year + "-" + month + "-" + day + " " + hour + ":" + mins + ":" + sed;
    }

    private static void setHistoryTime(Context context, String mac, long time) {
        sharedPreferences = context.getSharedPreferences("historyTime",
                Context.MODE_PRIVATE);
        editor = sharedPreferences.edit();
        editor.putLong(mac, time);
        editor.commit();
        getHistoryTime(context, mac);
    }

    private static long getHistoryTime(Context context, String mac) {
        sharedPreferences = context.getSharedPreferences("historyTime",
                Context.MODE_PRIVATE);
        return sharedPreferences.getLong(mac, 0);
    }

