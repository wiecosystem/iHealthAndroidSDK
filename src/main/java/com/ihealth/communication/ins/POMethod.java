    static String changeOfflineData2JSON(Context con, String mac, String firmware, List<byte[]> listHistory)
            throws JSONException {
        JSONObject object = new JSONObject();
        JSONArray array = new JSONArray();
        final List<byte[]> offlineList = new ArrayList<>();
        String messageString = null;

        if (listHistory != null && listHistory.size() > 0) {
            for (byte[] element : listHistory) {
                // 判断是否为重复数据或者错乱数据
                if (isWrongTime(con, element, mac, firmware)) {
                } else {
                    offlineList.add(element);
                }
            }
        }
        if (offlineList.size() > 0) {
            for (byte[] bs : offlineList) {
                String str = getOfflinedate(bs);
                str = changeOfflineData(con, mac, firmware, str);
                long measureTime = ByteBufferUtil.BirthdayToLong(str);
                int oxygen = (int) bs[7] & 0xFF;
                int pulseRate = ((int) bs[8] & 0xFF);
                if (pulseRate > 250) {
                    pulseRate = 250;
                } else if (pulseRate < 30) {
                    pulseRate = 30;
                }
                if (oxygen > 99) {
                    oxygen = 99;
                } else if (oxygen < 70) {
                    oxygen = 70;
                }
                String dataId = PublicMethod.getDataID(mac, oxygen + "", measureTime);
                JSONObject stoneObject = new JSONObject();
                stoneObject.put(PoProfile.DATAID, MD5.md5String(dataId));
                stoneObject.put(PoProfile.MEASURE_DATE_PO, str);
                stoneObject.put(PoProfile.BLOOD_OXYGEN_PO, oxygen);
                stoneObject.put(PoProfile.PULSE_RATE_PO, pulseRate);
                JSONArray array1 = new JSONArray();
                for (int i = 0; i < (bs.length - 9) / 2; i++) {
                    array1.put((bs[2 * i + 9] & 0xFF) + (bs[2 * i + 1 + 9] & 0xFF) * 256);
                }
                stoneObject.put(PoProfile.PULSE_WAVE_PO, array1);
                array.put(stoneObject);
            }
            object.put(PoProfile.OFFLINEDATA_PO, array);
            messageString = object.toString();
        }
        offlineList.clear();
        return messageString;
    }

